int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	q;

	i = 0;
	q = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
			++q;
		++i;
	}
	return (q);
}
